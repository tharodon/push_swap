#include "../push_swap.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*r;

	i = 0;
	r = (char *)s;
	if (n != 0)
	{
		while (i < n)
			r[i++] = '\0';
	}
}
